-- ============================================================
-- 角色权限修正：区分管理员与普通用户
--   1. 社区居民(role_id=3) 移除公告管理全部权限
--   2. 社区居民(role_id=3) 移除访客审批/签离/删除权限
--   3. 社区居民 仅保留：查看自己的访客申请 + 新增访客 + 通行记录
-- ============================================================
USE `com_ser`;

-- 一、居民：移除信息发布（公告管理）全部菜单
DELETE FROM `sys_role_menu`
WHERE `role_id` = 3
  AND `menu_id` IN (106, 10601, 10602, 10603, 10604, 10605, 10606);

-- 二、居民：移除访客管理中的管理权限（审批/签离/删除）
DELETE FROM `sys_role_menu`
WHERE `role_id` = 3
  AND `menu_id` IN (10503, 10504, 10505, 10507);
